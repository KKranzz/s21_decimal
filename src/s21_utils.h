const char* s21_get_power_of_two(s21_poweroftwo power) {
  static char res[32] = {0};

  const char* value = "0";

  if (power >= ZERO && power <= THIRTY_TWO) {
    value = s21_get_power_of_two_1(power);
  } else if (power <= SIXTY_FIVE) {
    value = s21_get_power_of_two_2(power - THIRTY_THREE);
  } else if (power <= NINETY_FIVE) {
    value = s21_get_power_of_two_3(power - SIXTY_SIX);
  }

  strncpy(res, value, sizeof(res) - 1);
  res[sizeof(res) - 1] = '\0';
  return res;
}
